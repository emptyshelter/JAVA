
/*
 * 4개의 멤버메소드를 가짐
 */
public class Calculator {
	/*
	 * add(두개의 정수)
	 * subtract
	 * divide
	 * multiple
	 */
	
	int add(int a, int b) {
		return a+b;
	}
	
	int subtract(int a, int b) {
		return a-b;
	}
	
	double divide(double a, double b) {
		return a/b;
	}
	
	int multiple(int a, int b) {
		return a*b;
	}
	
	
	
}
